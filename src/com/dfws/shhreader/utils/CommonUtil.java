package com.dfws.shhreader.utils;

import com.dfws.shhreader.R;

import android.content.Context;

public class CommonUtil {

	/**
	 * 读取  自定义分享文字
	 * @param context
	 * @return
	 */
	public static String GetSmsContent(Context context)
	{
		String sms_content = "";
		if(("").equals(sms_content))
		{
			sms_content = context.getResources().getString(R.string.share_sms_content);
		}
		
		return sms_content;
	}
}
