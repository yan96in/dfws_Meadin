  /**网页重新加载*/
            function setReloadListener(){
                var obj = document.getElementById("img_reload");
                if (obj) {
                      obj.onclick=function(){
                         window.newsbean.onReloadWeb();
                      }
                }
                
            }

            //初始化
						
            window.onload = function(){
            	setReloadListener();
            }